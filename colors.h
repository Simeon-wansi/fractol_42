#ifndef COLORS_H
# define COLORS_H

// Basic Colors
#define BLACK       0x00000000
#define WHITE       0x00FFFFFF
#define RED         0x00FF0000
#define GREEN       0x0000FF00
#define BLUE        0x000000FF

// Smooth Gradient Colors for Fractals
#define DEEP_BLUE       0x0000082E
#define MIDNIGHT_BLUE   0x00002040
#define OCEAN_BLUE      0x00004080
#define SKY_BLUE        0x000080FF
#define ELECTRIC_BLUE   0x0000FFFF

#define DEEP_PURPLE     0x00200040
#define VIOLET          0x006000A0
#define MAGENTA         0x00FF00FF

#define SUNSET_ORANGE   0x00FF4500
#define GOLDEN_YELLOW   0x00FFD700
#define BRIGHT_YELLOW   0x00FFFF00

// Darker Shades for Depth Effects
#define DARK_RED        0x008B0000
#define DARK_GREEN      0x00006400
#define DARK_BLUE       0x0000008B
#define DARK_PURPLE     0x004B0082

// Cool Tones for Smooth Gradients
#define TEAL            0x00008080
#define TURQUOISE       0x0040E0D0
#define AQUA            0x0000FFFF

// Soft Pastels (for smooth transitions)
#define PASTEL_PINK     0x00FFB6C1
#define PASTEL_LAVENDER 0x00E6E6FA
#define PASTEL_ORANGE   0x00FFDAB9

// Greyscale for Shading Effects
#define LIGHT_GRAY      0x00D3D3D3
#define MEDIUM_GRAY     0x00808080
#define DARK_GRAY       0x00404040

# endif